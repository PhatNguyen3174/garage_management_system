    private void load_nv(){
        DefaultTableModel dtmNV = (DefaultTableModel)tblNhanVien.getModel();
        ArrayList<NhanVienPoJo> dsNV = NhanVienDAO.layDSNhanVien();
        dtmNV.setRowCount(0);
        for(NhanVienPoJo nv: dsNV){
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getMaNV());
            vec.add(nv.getTennv());
            vec.add(nv.getTenDN());
            vec.add(nv.getPass());
            vec.add(nv.getSdt());
            vec.add(nv.getDiachi());
            dtmNV.addRow(vec);           
        }        
    }
    private void load_cv(){       
         cbxCV.removeAllItems();
        try {
            String sql = "select macv from chucvu";
            ResultSet rs = db.excuteQuery(sql);
            while(rs.next()){
                String items = rs.getString("MACV");
                cbxCV.addItem(items);
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
     private void load_anh(String id) {
        try { 
            String sql = "select anh from qlnx.nhanvien where manv= '"+id+"'";
            ResultSet rs = db.excuteQuery(sql);
          //  JOptionPane.showMessageDialog(rootPane, id);
            if (rs.next()) {
                byte[] img = rs.getBytes(1);
                ImageIcon myImage = new ImageIcon(img);
                Image img1 = myImage.getImage();
                Image newImg = img1.getScaledInstance(273, 289, Image.SCALE_SMOOTH);
                ImageIcon image = new ImageIcon(newImg);
                lblAnh.setIcon(image);
            }

            //cảm ơn bro nha
        } catch (Exception e) {
             JOptionPane.showMessageDialog(rootPane, "Error"+ e);
        }
    }
