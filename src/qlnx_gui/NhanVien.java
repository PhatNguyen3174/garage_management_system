    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:
        String manv = txtMa.getText();
        DefaultTableModel dtmNV = (DefaultTableModel)tblNhanVien.getModel();
        ArrayList<NhanVienPoJo> dsNV = NhanVienDAO.timKiemNVtheoTen(manv);
        dtmNV.setRowCount(0);
        for(NhanVienPoJo nv: dsNV){
            Vector<Object> vec = new Vector<Object>();
            vec.add(nv.getMaNV());
            vec.add(nv.getTennv());
            vec.add(nv.getTenDN());
            vec.add(nv.getPass());
            vec.add(nv.getSdt());
            vec.add(nv.getDiachi());
            dtmNV.addRow(vec);           
        }       
        
                         
        
    }//GEN-LAST:event_btnTimActionPerformed

