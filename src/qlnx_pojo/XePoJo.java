package qlnx_pojo;

/**
 *
 * @author phatnguyen
 */
public class XePoJo {
    private String maXe, maNV,tenXe, bienSo, anh;

    public XePoJo() {
    }

    public XePoJo(String maXe, String maNV, String tenXe, String bienSo, String anh) {
        this.maXe = maXe;
        this.maNV = maNV;
        this.tenXe = tenXe;
        this.bienSo = bienSo;
        this.anh = anh;
    }
    

    public String getMaXe() {
        return maXe;
    }

    public void setMaXe(String maXe) {
        this.maXe = maXe;
    }

    public String getMaNV() {
        return maNV;
    }

    public void setMaNV(String maNV) {
        this.maNV = maNV;
    }

    public String getTenXe() {
        return tenXe;
    }

    public void setTenXe(String tenXe) {
        this.tenXe = tenXe;
    }

    public String getBienSo() {
        return bienSo;
    }

    public void setBienSo(String bienSo) {
        this.bienSo = bienSo;
    }

    public String getAnh() {
        return anh;
    }

    public void setAnh(String anh) {
        this.anh = anh;
    }
    
    
    
    
}
